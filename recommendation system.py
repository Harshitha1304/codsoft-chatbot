def recommend_items(preferences, choice):
    recommended_items = []
    if choice == 1:  # Books
        for item, details in books_data.items():
            if (details["Language"] in preferences or any(author.strip() in preferences for author in details["author"].split(',')) or item in preferences) and item not in recommended_items:
                recommended_items.append(item)
    elif choice == 2:  # Songs
        for item, details in songs_data.items():
            if (details["movie"] in preferences or details["artist"] in preferences or item in preferences) and item not in recommended_items:
                recommended_items.append(item)
    elif choice == 3:  # Movies
        for key, details in movie_data.items():
            movie_titles = key.split(",")
            for movie_title in movie_titles:
                if (details["genre"] in preferences or details["Director"] in preferences or movie_title.strip() in preferences) and movie_title.strip() not in recommended_items:
                    recommended_items.append(movie_title.strip())
    if not recommended_items:
        print("Sorry, no items match your preferences.")
    return recommended_items

